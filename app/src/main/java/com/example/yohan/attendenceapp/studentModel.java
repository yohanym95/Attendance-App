package com.example.yohan.attendenceapp;

public class studentModel {

    public String stuName;
    public String stuCourse;
    public String stuRegNo;
    public String stuEmail;

    public studentModel(){

    }


    public studentModel(String stuName, String stuCourse, String stuRegNo, String stuEmail) {
        this.stuName = stuName;
        this.stuCourse = stuCourse;
        this.stuRegNo = stuRegNo;
        this.stuEmail = stuEmail;

    }

}
